    <h2>What's the difference between a standard Pidyon HaBen and a "Self Pidyon"?</h2>
    <p>The main difference is to who has the obligation, and needs to perform the ceremony. When a firstborn son is born
        to parents who aren't kohanim or Levi'im, the obligation is on the father to perform the ceremony on the 30th
        day after the son is born or anytime after if that deadline is missed for any reason.</p>
    <p>However, if the firstborn reaches bar mitzvah at 13 and the father did not perform the ceremony, the obligation
        falls upon the individual to redeem himself, resulting in a "Self Pidyon". In this case, the ceremony is a bit
        different and won't be found in a siddur or in the Chabad article I linked above. That is what this site is for.
        The main page of this side shows the the part of the ceremony which is different when the firstborn is redeeming
        himself.</p>
    
