    <h2>What is a "Self Pidyon"?</h2>
    <p>Redemption of the firstborn is an important mitzvah from from the Torah which calls on a father to redeem a
        firstborn son by paying 5 silver sla'im (shekels) to a Kohen.</p>
    <p>Sometimes however, due to a number of circumstances this doesn't happen, and when the firstborn reaches bar
